interface LinkSeoContext {
  type: "product" | "establishment" | "service" | "article" | "category" | "action" | "navigation";
  name?: string;
  action?: "view" | "compare" | "review" | "order" | "contact" | "learn";
}

const actionLabels: Record<NonNullable<LinkSeoContext["action"]>, string> = {
  view: "Переглянути",
  compare: "Порівняти",
  review: "Читати відгуки про",
  order: "Замовити",
  contact: "Зв'язатися з",
  learn: "Дізнатися про",
};

/**
 * Generate SEO-optimized anchor/aria-label for links
 */
export function generateSeoAnchor(context: LinkSeoContext): string {
  const { type, name, action = "view" } = context;
  
  const actionLabel = actionLabels[action];
  
  switch (type) {
    case "product":
      return `${actionLabel} ${name || "продукт"} - детальна інформація та відгуки`;
    case "establishment":
      return `${actionLabel} ${name || "заклад"} - меню, ціни, відгуки`;
    case "service":
      return `${actionLabel} ${name || "сервіс"} - функції та тарифи`;
    case "article":
      return `Читати статтю: ${name || ""}`;
    case "category":
      return `Перейти до категорії ${name || ""}`;
    case "navigation":
      return `Перейти до розділу ${name || ""}`;
    case "action":
      return `${actionLabel} ${name || ""}`;
    default:
      return name || "Перейти";
  }
}

